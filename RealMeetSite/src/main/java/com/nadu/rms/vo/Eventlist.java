package com.nadu.rms.vo;

public class Eventlist {
	private String elidx;
	private String esidx;
	private String addr;
	private String date;
	private int maxgnum;
	
	public int getMaxgnum() {
		return maxgnum;
	}
	public void setMaxgnum(int maxgnum) {
		this.maxgnum = maxgnum;
	}
	public String getElidx() {
		return elidx;
	}
	public void setElidx(String elidx) {
		this.elidx = elidx;
	}
	public String getEsidx() {
		return esidx;
	}
	public void setEsidx(String esidx) {
		this.esidx = esidx;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
